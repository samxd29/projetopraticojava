public class Fluxo {

    public static void main(String[] args) {
        System.out.println("Ini do main");
        try{
            metodo1();
        } catch(Exception ex) {
            String msg = ex.getMessage();
            System.out.println("Exception " + msg);
            ex.printStackTrace();
        } 
        System.out.println("Fim do main");
    }

    private static void metodo1() {
        System.out.println("Ini do metodo1");
        metodo2();
        System.out.println("Fim do metodo1");
    }

    private static void metodo2() {
        System.out.println("Ini do metodo 2");
        metodo2();    
        System.out.println("Fim do metodo 2");
    }
  //Exceção : Para tratarmos uma exceção, é preciso sinalizar para a máquina virtual que isso pode acontecer,
  //por meio de um código específico (try). Assim, ela entenderá que deve tentar executar esse código, entre chaves ({})
}    

